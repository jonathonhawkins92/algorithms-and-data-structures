function areThereDuplicates(...arr) {
  let i = 0;
  if (arr.length === 0) {
    return false;
  }
  arr.sort();
  for (let index = 1; index < arr.length; index++) {
    if (arr[i] === arr[index]) {
      return true;
    }
    i++;
  }
  return false;
}

